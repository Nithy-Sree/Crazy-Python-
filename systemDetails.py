import platform

# to know about the machine in your system
print(platform.machine())

# to know the name of your system
print(platform.node())

# to know which version of python you are using in your system
print(platform.python_branch())

# to know the architecture
print(platform.architecture())

# to know the version of windows
print(platform.version())

# all the details
print(platform.uname())
